// Реализовать четыре основные арифметические операции в виде функций с двумя параметрами.
// Обязательно использовать оператор return.

function simpleAddition(a, b) {
    return a + b;
}

function simpleDiminution(a, b) {
    return a - b;
}

function simpleMultiplication(a, b) {
    return a * b;
}

function simpleDivision(a, b) {
    return a / b;
}

console.log(simpleAddition(4, 2));
console.log(simpleDiminution(4, 2));
console.log(simpleMultiplication(4, 2));
console.log(simpleDivision(4, 2));